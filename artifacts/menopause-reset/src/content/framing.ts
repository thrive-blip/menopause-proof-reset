export function renderLetter(name: string): string {
  const first = name.trim() || "there";
  return `Dear ${first},

If you're like a lot of the women I work with, something changed when perimenopause or menopause arrived, and work suddenly got so much harder.

Maybe it shows up as forgetting things, or losing your train of thought mid-sentence. Maybe the work that used to take you an hour now eats the whole day. Or maybe you're just tired in a way that sleep doesn't touch. It looks different for every woman, but the thread underneath is the same. You're working so much harder to do what used to come easy.

You've probably already connected it to your hormones, and you're right to, that's a real part of this. But there's another piece almost no one talks about.

The way you've been working, the pace, the always-on, the pushing through, was always expensive. It cost you even at 30. You just had the reserves to cover it back then, so you could muscle through and make it work.

The hormonal shift is what takes those reserves away. As your hormones change, so does your energy, your focus, your bandwidth, how your brain works. Your body can't push through the way it used to, especially when so much of how you work is draining you or demanding more than you have.

So the cost that was always there, the cost of working against the way you're built, starts showing up.

That's the MISMATCH. Your body used to cover for a way of working that never quite fit, and now it can't.

So this document does two things. First, it shows you how you're built to work, the real you, the one who's still in there. A way of working that doesn't keep draining the nervous system, brain, and bandwidth that perimenopause or menopause has already left running low. Then it gives you your reset: three small things you can try tomorrow, and how to lean into this way of working over the coming weeks.

Because when you stop pouring energy you don't have into a way of working that fights how you're built, you start to feel the cost of the mismatch ease. You have something left at the end of the day. The 2pm crash softens. Work starts to feel more in flow. And you feel more capable, more like yourself again.

And here's something most women are never told about this stage. It can be so much more than something to get through. When you start to see how you're built, and work with it instead of against it, the hidden friction you've been carrying for years begins to lift. That gives you back time, energy, and capacity you never knew you were spending, and it honours the way you've always been wired. This can become one of the clearest, most aligned stretches of your whole working life.

Read it slow, and keep what fits and leave the rest. You don't have to do it all, or even most of it. You just have to start seeing yourself clearly, and everything good starts there.

Talk soon,
Dr. Adrienne, ND`;
}

